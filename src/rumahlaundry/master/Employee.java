    public boolean doValidate() {
        boolean valid = false;
        if (MyHelper.validateTextRequired(textUsername)
                && MyHelper.validateTextRequired(textNamaKaryawan)
                && MyHelper.validateTextRequired(textKontakKaryawan)
                && MyHelper.validateComboboxRequired(jComboBoxStatus)
                && MyHelper.validateTextAreaRequired(textAreaAlamat)) {
            valid = true;
        }

        return valid;
    }
    
    private void buttonSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSaveActionPerformed
        if (!doValidate()) {
            return;
        }
        try {
            String sql = "INSERT INTO MASTER_EMPLOYEE (USERNAME, NAME, CONTACT, ADDRESS, STATUS, PASSWORD) VALUES (?,?,?,?,?,?)";
            PreparedStatement stat = connection.prepareStatement(sql);
            stat.setString(1, textUsername.getText());
            stat.setString(2, textNamaKaryawan.getText());
            stat.setString(3, textKontakKaryawan.getText());
            stat.setString(4, textAreaAlamat.getText());
            stat.setString(5, jComboBoxStatus.getSelectedItem().toString());
            stat.setString(6, textUsername.getText() + "123");
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan");
            doReset();
            textUsername.requestFocus();
            dataTable();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Data Gagal Disimpan" + e.getMessage());
        }
    }//GEN-LAST:event_buttonSaveActionPerformed

    private void buttonEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonEditActionPerformed
        if (!doValidate()) {
            return;
        }
        try {
            String sql = "UPDATE MASTER_EMPLOYEE SET USERNAME = ?, NAME = ?, CONTACT = ?, ADDRESS = ?, STATUS = ?"
            + " where ID = ?";
            PreparedStatement stat = connection.prepareStatement(sql);

            stat.setString(1, textUsername.getText());
            stat.setString(2, textNamaKaryawan.getText());
            stat.setString(3, textKontakKaryawan.getText());
            stat.setString(4, textAreaAlamat.getText());
            stat.setString(5, jComboBoxStatus.getSelectedItem().toString());
            stat.setString(6, textIDKaryawan.getText());

            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data berhasil diubah");
            doReset();
            textUsername.requestFocus();
            dataTable();
        } catch (SQLException e){
            JOptionPane.showMessageDialog(null, "Data gagal diubah" + e);
        }
    }//GEN-LAST:event_buttonEditActionPerformed

