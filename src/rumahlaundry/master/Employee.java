    private void dataTable() {
        Object[] Baris = {"ID", "Username", "Nama", "Kontak", "Status",  "Alamat"};
        tabmode = new DefaultTableModel(null, Baris);
        tableKaryawan.setModel(tabmode);
        try {
            java.sql.Statement stat = connection.createStatement();
            String sql = "SELECT * FROM MASTER_EMPLOYEE ORDER BY ID";
            ResultSet result = stat.executeQuery(sql);
            if (result != null) {
                while (result.next()) {
                    String id = result.getString("id");
                    String username = result.getString("username");
                    String nama = result.getString("name");
                    String kontak = result.getString("contact");
                    String status = result.getString("status");
                    String alamat = result.getString("address");
                    String[] data = {id, username, nama, kontak, status, alamat};
                    tabmode.addRow(data);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Data Karyawan tidak ditemukan");
                doReset();
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }

